export class bowlGame {
  constructor(
    bowlName,
    date,
    homeTeam,
    awayTeam,
    homeTeamLine,
    awayTeamLine,
    homeScore,
    awayScore
  ) {
    this.bowlName = bowlName;
    this.date = date;
    this.homeTeam = homeTeam;
    this.awayTeam = awayTeam;
    this.homeTeamLine = homeTeamLine;
    this.awayTeamLine = awayTeamLine;
    this.homeScore = homeScore;
    this.awayScore = awayScore;
  }
}
